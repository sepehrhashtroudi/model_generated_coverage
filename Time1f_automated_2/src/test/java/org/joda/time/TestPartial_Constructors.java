     assertEquals(0, result.get(DateTimeFieldType.halfdayOfDay())); 
     assertEquals(0, result.indexOf(DateTimeFieldType.era())); 
     assertEquals(1, result.indexOf(DateTimeFieldType.halfdayOfDay())); 
